package com.webzino.milkdelightuser.Model;

public class Menu_Model {
    int image;
    String title;

    public Menu_Model(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
